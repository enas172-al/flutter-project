class LegalCase {
  final String id;
  final String title;
  final String number;
  final String court;
  final String lawyer;
  final String nextSession;
  final String type;
  final String status;

  LegalCase({
    required this.id,
    required this.title,
    required this.number,
    required this.court,
    required this.lawyer,
    required this.nextSession,
    required this.type,
    required this.status,
  });
}
